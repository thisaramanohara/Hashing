public class Entry {

    private int key;
    private String value;

    //initialize the entry
    public Entry(int key, String value) {
        this.key = key;
        this.value = value;
    }

    //get the key
    public int getKey() {
        return this.key;
    }

    //get the value
    public String getValue() {
        return this.value;
    }

}
